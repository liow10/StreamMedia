package com.micro.iotclouds.modules.rtmp.client.service;

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStream;
import java.io.InputStreamReader;

import com.micro.iotclouds.modules.rtmp.client.domain.CmdTask;
import com.micro.iotclouds.modules.rtmp.client.domain.Global;

import lombok.Data;
import lombok.extern.slf4j.Slf4j;

/**
 * <p>Copyright(c) 2019. Pnoker All Rights Reserved.
 * <p>@Author    : Pnoker
 * <p>Email      : pnokers@gmail.com
 * <p>Description: Process输出处理线程服务
 */
@Data
@Slf4j
public class CmdOutputService implements Runnable {
    /**
     * 任务 ID
     */
    private String id;
    private volatile boolean status = true;
    private Process process;

    public CmdOutputService(String id, Process process) {
        this.id = id;
        this.process = process;
    }

    @Override
    public void run() {
        read(process.getErrorStream());
    }

    /**
     * 读取 CMD 执行输出
     *
     * @param inputStream
     */
    public void read(InputStream inputStream) {
        BufferedReader reader = new BufferedReader(new InputStreamReader(inputStream));
        String line;
        try {
            while (null != (line = reader.readLine()) && status) {
                //log..info(line);
                handle(line.toLowerCase());
            }
            if (!status) {
                Global.taskMap.get(id).clear();
            }
        } catch (IOException e) {
            log.error(e.getMessage(), e);
        } finally {
            try {
                inputStream.close();
            } catch (IOException e) {
                log.error(e.getMessage(), e);
            }
            reStartTask();
        }
    }

    /**
     * 通过输出内容检测是否运行错误，从而判断是否需要重启 CMD 任务
     *
     * @param message
     */
    public void handle(String message) {
        if (message.contains("fail") || message.contains("miss") || message.contains("error")) {
            log.error(message);
            log.info("准备重启 task {}", id);
            status = false;
            reStartTask();
        }
    }

    /**
     * 重启错误 CMD 任务
     */
    public void reStartTask() {
        CmdTask cmdTask = Global.taskMap.get(id);
        cmdTask.clear();
        cmdTask.create();
        cmdTask.setStatus(2);
    }

}